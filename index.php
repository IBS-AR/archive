<div class="pagename"><h1>Welcome!</h1></div>

<div class="text">
	<p>Welcome to the website for the Australasian
	Region of The International Biometric Society.</p>
	
<p>Our society comprises members of <a href="http://www.tibs.org">The International Biometric Society</a> that predominantly reside in Australia and New Zealand. In 2006, membership numbers were over 170.</p>
	
<p>Professionally, we are statisticians, biometricians,
mathematicians, scientists and managers, from academia, consulting or
research and development, with biological applications being the common
link. Biological applications include medicine, public health, ecology,
environmental science, agriculture, genetics, microbiology and
toxicology, amongst others.</p>

<p>Our employers range from local and commonwealth
government agencies where there may be a team of biometricians employed,
to private institutions which may only employ one biometrician.</p>

<p>Our main activity is our <a href="conferences.html">biennial conference</a>,
which will next take place in December 2007. However we also offer
students <a href="prizes.html">scholarships</a> and financial support to attend conferences on a
regular basis.</p>


